public class Force {
    protected double forceX;
    protected double forceY;

    public Force(double forceX,double forceY){
        this.forceX = forceX;
        this.forceY = forceY;
    }

    public double getForceX() {
        return forceX;
    }

    public double getForceY() {
        return forceY;
    }
}
